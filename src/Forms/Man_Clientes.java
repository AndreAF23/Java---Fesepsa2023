    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String ruc = jTextField3.getText(),telefono1=jTextField8.getText(),telefono2=jTextField9.getText(),
                ubigeo=jTextField10.getText(),codclie=jTextField1.getText();
        if(ruc.length()==11){
            if(telefono1.length()<=9){
                if(telefono2.length()<=9){
                    if(ubigeo.length()==6){
                        String consulta="EXEC [dbo].[AF_MANAGE_CRUDCLIE] 4,'','','" + codclie + "','','','" + ruc + "','" 
                                + jTextField4.getText() + "','" + jTextField5.getText() + "','" + jTextField6.getText() + "','"
                                 + jTextField7.getText() + "','" + telefono1 + "','" + telefono2 + "','" + ubigeo + "'";
                        EjecutarQuery exec = new EjecutarQuery();
                        exec.ejecutar(consulta);
                        JOptionPane.showMessageDialog(null,"Datos del cliente actualizados");
                    }else{
                        JOptionPane.showMessageDialog(null,"Revisar Ubigeo");
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"Revisar Telefono 2");
                }
            }else{
                JOptionPane.showMessageDialog(null,"Revisar Telefono 1");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Revisar RUC");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

