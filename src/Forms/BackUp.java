    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String consulta="",respuesta="",bd="";
            
            
            //prepara datos para la consulta
            bd=jComboBox1.getSelectedItem().toString();
            
            ConvertirFecha conversion = new ConvertirFecha();
            Date fecha1=jDateChooser1.getDate();
            String fecha= fecha1.toString();
            fecha=conversion.convertir(fecha);
            fecha=fecha.substring(0,2) + fecha.substring(3,5) + fecha.substring(8,10);
            System.out.println(fecha);
            consulta="EXEC [dbo].[AF_BACKUP] '" + bd + "','" + fecha + "'";
            System.out.println(consulta);
            //ejecuta consulta y trae respuesta para mostrarla en jOptionPane
            EjecutarQuery exec = new EjecutarQuery();
            respuesta = exec.ejecutar(consulta);
            JOptionPane.showMessageDialog(null , respuesta);
        }catch(NullPointerException ex){
            JOptionPane.showMessageDialog(null,"Revisar fecha");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

