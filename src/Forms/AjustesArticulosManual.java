    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int filas = 0;
        String consulta="",res="",rpta="";
        filas = jTable1.getRowCount();
        if(filas==0){
            JOptionPane.showMessageDialog(null,"Ingrese registros para realizar el ajuste");
        }else{
            for(int i=0;i<filas;i++){
                EjecutarQuery exec = new EjecutarQuery();
                consulta="EXEC [dbo].[JM_REGISTER_AJUSTE_MANUAL] '" + jTable1.getValueAt(i,0) + "','" + jTable1.getValueAt(i,1) + "','" + jTable1.getValueAt(i,2) + "'," + jTable1.getValueAt(i,3) + ";";
                System.out.println(consulta);
                res=exec.ejecutar(consulta);
                rpta=rpta + res + "\n";
            }
        }
        GenerarLog gen = new GenerarLog();
        gen.Log("RegAjusteArticulo","03_AJUSTE", rpta);
        JOptionPane.showMessageDialog(null,"Ajuste realizado correctamente");
    }//GEN-LAST:event_jButton4ActionPerformed

