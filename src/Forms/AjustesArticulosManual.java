    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese Codigo");
        }else{
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
            VerificarCodUnit ver = new VerificarCodUnit();
            String codigo="",res="",tipo="",documento="";
            int cantidad = 0;
            //convertir fecha
            ConvertirFecha conversion = new ConvertirFecha();
            Date fecha1=jDateChooser2.getDate();
            String fecha= fecha1.toString();
            fecha=conversion.convertir(fecha);
            //System.out.println(fecha);
            //convertir fecha
            codigo = jTextField1.getText().trim();
            res=ver.ver(codigo);

            //tipo de ajuste (entrada o salida)

            tipo=(String)jComboBox1.getSelectedItem();

            if(tipo.equals("Entrada")){
                tipo="E";
            }else{
                tipo="S";
            }

            cantidad = (int)jSpinner1.getValue();
            if(res.contains("no")){
                JOptionPane.showMessageDialog(null,"Codigo no existe");
            }else{
                if(cantidad<=0){
                    JOptionPane.showMessageDialog(null,"Cantidad Debe ser mayor 0");
                }else{
                    documento="777" + tipo + "0" + fecha.substring(0, 2) + fecha.substring(3, 5) + fecha.substring(8, 10);
                    modelo.addRow(new Object[]{documento,fecha,codigo,cantidad});
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

