        codigo = (String)jTable1.getValueAt(selec,0);
        consulta = "EXEC [dbo].[AF_MANAGE_CRUDUSUARIOS] 3,'" + codigo + "','','',''";
        EjecutarQuery exec = new EjecutarQuery();
        String res=exec.ejecutar(consulta);
