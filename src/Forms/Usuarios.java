    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String consulta = "";
        int selec = 0;
        selec = jTable1.getSelectedRow();
        if(selec==-1){
            JOptionPane.showMessageDialog(null,"Seleccione un registro");
        }else{
            int input = JOptionPane.showConfirmDialog(null,"Desea eliminar usuario de " + (String)jTable1.getValueAt(selec,2), "Confirmación", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(input==0){
                eliminarregistro(selec);
            }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

