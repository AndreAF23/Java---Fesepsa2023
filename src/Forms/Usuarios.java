        codigo=jTextField1.getText().trim();
        usuario=jTextField2.getText().trim();
        nombre=jTextField3.getText().trim();
        rol=jTextField4.getText().trim();
        consulta="EXEC [dbo].[AF_MANAGE_CRUDUSUARIOS] 1,'" + codigo + "','" + usuario + "','" + nombre + "','" + rol + "'";
