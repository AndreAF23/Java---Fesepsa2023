    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        int selec = 0;
        selec=jTable1.getSelectedRow();
        if(selec!=-1){
            jTextField1.setText((String)jTable1.getValueAt(selec,0));
            jTextField2.setText((String)jTable1.getValueAt(selec,1));
            jTextField3.setText((String)jTable1.getValueAt(selec,2));
            jTextField2.setEditable(true);
            jTextField3.setEditable(true);
        }else{
            jTextField2.setEditable(false);
            jTextField3.setEditable(false);
        }
    }//GEN-LAST:event_jTable1MousePressed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String fecha="",venta="",compra="",consulta="";
        fecha=jTextField1.getText();
        compra=jTextField2.getText();
        venta=jTextField3.getText();
        if(fecha.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese una fecha para actualizar");
        }else{
            EjecutarQuery exec = new EjecutarQuery();
            consulta = "EXEC [dbo].[AF_MANAGE_TCAMBIO] '3','" + fecha + "','','" + compra + "','" + venta + "'";
            JOptionPane.showMessageDialog(null, exec.ejecutar(consulta));
            consultar();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

