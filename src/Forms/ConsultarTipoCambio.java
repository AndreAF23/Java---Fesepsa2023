    public void eliminardatos(){
        DefaultTableModel modelo = (DefaultTableModel)jTable1.getModel();
        for(int i = 0;i<jTable1.getRowCount();i++){
            modelo.removeRow(i);
        }
    }
    
    public void limpiardatos(){
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
    }
    
    public void consultar(){
        try{
            String fecha1="",fecha2="",fecha="",consulta="";
            ConvertirFecha convert = new ConvertirFecha();

            //conversion fecha desde
            Date fechadate=jDateChooser1.getDate();
            fecha1=convert.convertir(fechadate.toString());

            //conversion fecha hasta
            fechadate=jDateChooser2.getDate();
            fecha2=convert.convertir(fechadate.toString());

            //realiza consulta y llena tabla
            consulta = "EXEC [dbo].[AF_MANAGE_TCAMBIO] '1','" + fecha1 + "','" + fecha2 + "'";
            LlenarTabla llenx = new LlenarTabla();
            llenx.llenar(jTable1, consulta);
            
        }catch(NullPointerException ex){
            JOptionPane.showMessageDialog(null,"Revise las fechas");
        }
    }
    
