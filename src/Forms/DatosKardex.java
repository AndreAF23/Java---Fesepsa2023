    public void llenarcombobox(){
        for (int i=anio;i>=2022;i--){
            jComboBox1.addItem(String.valueOf(i));
        }
        llenarmesactual();
    }
    public void llenartodosmeses(){
        DevolverMesText dev = new DevolverMesText();
        jComboBox2.removeAllItems();
        for(int i=12;i>=1;i--){
            jComboBox2.addItem(dev.devolver(i));
        }
    }
    public void llenarmesactual(){
        jComboBox2.removeAllItems();
        DevolverMesText dev = new DevolverMesText();
        for (int i=mes;i>=1;i--){
            jComboBox2.addItem(dev.devolver(i));
        }
    }
    
