    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DevolverMesNum dev = new DevolverMesNum();
        LlenarTabla llenx = new LlenarTabla();
        String consulta="";
        int messelc=Integer.parseInt(dev.dev((String)jComboBox2.getSelectedItem()));
        int anioselc = Integer.parseInt((String)jComboBox1.getSelectedItem());
        
        consulta = "EXEC [dbo].[AF_DATOSKARDEX] '4'," + anioselc + "," + messelc + "";
        llenx.llenar(jTable1, consulta);
        
        consulta = "EXEC [dbo].[AF_DATOSKARDEX] '2'," + anioselc + "," + messelc + "";
        llenx.llenar(jTable2, consulta);
        
        ExportarExcelKardex obj;
        
        try{
            obj= new ExportarExcelKardex();
            obj.exportarExcel(jTable1,jTable2);
        }catch(IOException ex){
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

