    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Clases.Conecx con = new Clases.Conecx();
        Statement sql = null;
        
        String entrada="",desicion="",dato="";
        desicion = (String)jComboBox1.getSelectedItem();
        dato = jTextField1.getText();
        //"RUC", "Razón Social", "Dirección", "Distrito", "Provincia"
        
        switch(desicion){
            case "RUC":{
                entrada="RucProveedor";
                break;
            }
            case "Razón Social":{
                entrada="RazonSocialProveedor";
                break;
            }
            case "Dirección":{
                entrada="DireccionProveedor";
                break;
            }
            case "Distrito":{
                entrada="distrito";
                break;
            }
            case "Provincia":{
                entrada="provincia";
                break;
            }
        }
        
        try {
                sql = con.Conectarse().createStatement();
            } catch (SQLException ex) {
                Logger.getLogger(TipoCambio.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        
        
           
            String consulta = "EXEC [dbo].[AF_MANAGE_CRUDPROV] 1," + entrada + "," + dato ;
            System.out.println(consulta);
            try {
                ResultSet rs = sql.executeQuery(consulta);
                modelo.setRowCount(0);
                while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getString(4));
                v.add(rs.getString(5));
                modelo.addRow(v);      
                jTable1.setModel(modelo);
                
            }
                
            } catch (SQLException ex) {
                Logger.getLogger(Proveedores.class.getName()).log(Level.SEVERE, null, ex);
            }
          
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        String distrito="",provincia="";
        int seleccion = -1;
        seleccion=jTable1.getSelectedRow();
        
        if(seleccion!=-1){
            jTextField2.setText((String)jTable1.getValueAt(seleccion, 0));
            jTextField3.setText((String)jTable1.getValueAt(seleccion, 1));
            jTextField4.setText((String)jTable1.getValueAt(seleccion, 2));
            distrito=((String)jTable1.getValueAt(seleccion, 3));
            provincia=((String)jTable1.getValueAt(seleccion, 4));
        }
        Clases.Conecx con = new Clases.Conecx();
        Statement sql = null;
        
        try{
            jComboBox2.removeAllItems();
            sql = con.Conectarse().createStatement();
            String consulta="SELECT Provincia FROM Ubigeo WHERE Distrito='" + jComboBox3.getSelectedItem() + "' GROUP BY Provincia";
            ResultSet rs = sql.executeQuery(consulta);
            while(rs.next()){
               jComboBox2.addItem(rs.getString(1));
            }
            rs.close();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error: " + ex.toString());
        }
       
    }//GEN-LAST:event_jTable1MousePressed

