    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codigo = jTextField1.getText();
        String rpta="",cod="",descripcion="",procedencia="",marca="",consulta="";
        boolean pasar=true;
        VerificarVacios ver = new VerificarVacios();
        pasar=ver.verificar(jTable1);
        
        VerificarCodUnit verificar = new VerificarCodUnit();
        rpta=verificar.ver(codigo);
        
        cod = jTextField1.getText().trim();
        descripcion=jTextArea1.getText().trim();
        procedencia=(String)jComboBox1.getSelectedItem();
        marca=(String)jComboBox2.getSelectedItem();
        
        DevolverMarca devolver = new DevolverMarca();
        marca=devolver.dev(marca);
        
        
        if(cod.length()>8){
            JOptionPane.showMessageDialog(null,"El codigo ingresado debe tener como máximo 8 digitos");
        }else{
            if(rpta.contains("no")){
                if(jCheckBox1.isSelected()){
                    if(pasar==false){
                        JOptionPane.showMessageDialog(null,"Verificar Celdas vacías en tabla");
                    }else{
                        consulta = "Exec [dbo].[AF_MANAGE_CRUDART] 4,'','','" + cod + "','" + descripcion + "','','" + marca + "','" + procedencia + "','','1'";
                        
                        boolean ejecutar = crearcombo();
                        if(ejecutar==true){
                            crearart(consulta);
                        }
                    } 
                }else{
                    consulta = "Exec [dbo].[AF_MANAGE_CRUDART] 4,'','','" + cod + "','" + descripcion + "','','" + marca + "','" + procedencia + "'";
                    crearart(consulta);
                    jTextField1.setText("");
                    jTextArea1.setText("");
                }
            }else{
                if(rpta.contains("si")){
                    JOptionPane.showMessageDialog(null,"Codigo Ya existe");
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

